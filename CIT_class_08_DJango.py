# Creative IT Class #08 Topic: env and django setup Date: 10/05/2023
# 1. Django Virtual Environment create
'''
CMD Command>>> python -m venv env
'''
# 2. Django Activate Environment
'''
CMD Command>>> env\Scripts\activate
'''
# 3. Install pip
'''
CMD Command>>> pip install django
'''
# 4. Django Create Project with "Manage.py"
'''
CMD Command>>> django-admin startproject project_name .
'''
# 5. Django Create Admin and Run Server
'''
CMD Command>>> python manage.py runserver
'''
